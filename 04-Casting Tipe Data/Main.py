# Casting (Konversi tipe data)

# Integer
print("=====INTEGER=====")
data_int = 5
print("Data 1 :", data_int, ", Type :", type(data_int)) 

data_float = float(data_int)
print("Data 2 :", data_float, ", Type :", type(data_float)) 

data_str = str(data_int)
print("Data 3 :", data_str, ", Type :", type(data_str)) 

data_bool = bool(data_int)
print("Data 4 :", data_bool, ", Type :", type(data_bool)) 

# Float
print("=====FLOAT=====")
data_float =  9.8
print("Data 1 :", data_float, ", Type :", type(data_float))

data_int = int(data_float)
print("Data 2 :", data_int, ", Type :", type(data_int))

data_str = str(data_float)
print("Data 3 :", data_str, ", Type :", type(data_str))

data_bool = bool(data_float)
print("Data 4 :", data_bool, ", Type :", type(data_bool))

# Boolean
print("=====BOOLEAN=====")
data_bool =  False
print("Data 1 :", data_bool, ", Type :", type(data_bool))

data_int = int(data_bool)
print("Data 2 :", data_int, ", Type :", type(data_int))

data_str = str(data_bool)
print("Data 3 :", data_str, ", Type :", type(data_str))

data_float = float(data_bool)
print("Data 4 :", data_float, ", Type :", type(data_float))

# String
print("=====STRING=====")
data_str =  "8"
print("Data 1 :", data_str, ", Type :", type(data_str))

data_int = int(data_str)
print("Data 2 :", data_int, ", Type :", type(data_int))

data_float = float(data_str)
print("Data 3 :", data_float, ", Type :", type(data_float))

data_bool = bool(data_str)
print("Data 4 :", data_bool, ", Type :", type(data_bool))

