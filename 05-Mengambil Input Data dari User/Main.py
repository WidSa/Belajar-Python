# Input data

data = input("Masukkan data: ") # data yang di input pasti string
print("Data:", data, ", Type:", type(data))

# jika mengambil data berupa int/float
angka = int(input("Masukkan data: "))
angka = float(input("Masukkan data: "))
print("Data:", angka, ", Type:", type(angka))

# Inpud data boolean
data_boolean = bool(int(input("Masukkan data: ")))
print("Data:", data_boolean, ", Type:", type(data_boolean))
